.MODEL SMALL
.STACK 200H
PRINT MACRO S
MOV AH,09H
MOV DX,OFFSET S
INT 21H
ENDM
DATA SEGMENT
MSG DB 'ENTER THE STRING:$'
MSG1 DB 'STRING IS PALINDROME$'
MSG2 DB 'STRING NOT PALINDROME$'
NL DB 0DH,0AH,'$'
STR DB 50 DUP('$')
DATA ENDS
CODE SEGMENT
ASSUME DS:DATA,CS:CODE
START:MOV AX,DATA
  MOV DS,AX
  MOV ES,AX
  PRINT MSG
  MOV AH,01H
  MOV SI,OFFSET STR
  MOV CX,0000H
  LOOP1:INT 21H
  CMP AL,0DH
  JZ STOP1
  MOV [SI],AL
  INC SI
  INC CX
  JMP LOOP1
  STOP1:DEC SI
  MOV DI,OFFSET STR
  MOV AX,CX
  MOV CL,02H
  DIV CL
  MOV CL,AL
  LOOP2:CMP CL,00H
  JZ PAL
  MOV AH,[SI]
  CMP AH,[DI]
  JNZ NOTPAL
  DEC SI
  INC DI
  DEC CL
  JMP LOOP2
  PAL:PRINT NL
  PRINT MSG1
  JMP SKIP
  NOTPAL:PRINT NL
  PRINT MSG2
  SKIP:MOV AH,4CH
  INT 21H
  CODE ENDS
  END START
